DROP TABLE IF EXISTS MultipleAssociations;

DROP TABLE IF EXISTS Customer;
CREATE TABLE Customer (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	PRIMARY KEY(id)
);

DROP TABLE IF EXISTS Dept;
CREATE TABLE Dept (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	PRIMARY KEY(id)
);

DROP TABLE IF EXISTS Users;
CREATE TABLE Users (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	PRIMARY KEY(id)
);

DROP TABLE IF EXISTS Profiles;
CREATE TABLE Profiles (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	PRIMARY KEY(id)
);

CREATE TABLE MultipleAssociations (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	profileId INT UNIQUE,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_PROFILES_0 FOREIGN KEY(profileId) REFERENCES Profiles(id),
	userId INT UNIQUE,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_USERS_0 FOREIGN KEY(userId) REFERENCES Users(id),
	profileId INT UNIQUE,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_DEPT_0 FOREIGN KEY(profileId) REFERENCES Dept(id),
	customerId INT UNIQUE,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_CUSTOMER_0 FOREIGN KEY(customerId) REFERENCES Customer(id),
	PRIMARY KEY(id)
);