DROP TABLE IF EXISTS MultipleAssociations;
DROP TABLE IF EXISTS User;
DROP TABLE IF EXISTS Profile;

CREATE TABLE Profile (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	PRIMARY KEY(id)
);

CREATE TABLE User (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	profileId INT,
	CONSTRAINT FK_USER_PROFILE_0 FOREIGN KEY(profileId) REFERENCES Profile(id),
	PRIMARY KEY(id)
);

CREATE TABLE MultipleAssociations (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	profileId INT,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_PROFILE_0 FOREIGN KEY(profileId) REFERENCES Profile(id),
	userId INT,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_USER_0 FOREIGN KEY(userId) REFERENCES User(id),
	PRIMARY KEY(id)
);
