DROP TABLE IF EXISTS MultipleAssociations;
CREATE TABLE MultipleAssociations (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	profileId INT,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_PROFILE_0 FOREIGN KEY(profileId) REFERENCES Profile(id),
	userId VARCHAR,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_VARCHAR_0 FOREIGN KEY(userId) REFERENCES VARCHAR(id),
	PRIMARY KEY(id)
);

DROP TABLE IF EXISTS Profiles;
CREATE TABLE Profiles (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	userId VARCHAR,
	CONSTRAINT FK_PROFILES_VARCHAR_0 FOREIGN KEY(userId) REFERENCES VARCHAR(id),
	PRIMARY KEY(id)
);

DROP TABLE IF EXISTS Users;
CREATE TABLE Users (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	PRIMARY KEY(id)
);