DROP TABLE IF EXISTS MedicalNeed;

DROP TABLE IF EXISTS MedicalItem;
CREATE TABLE MedicalItem (
	itemId INT NOT NULL AUTO_INCREMENT,
	string VARCHAR(191) NOT NULL,
	type VARCHAR(191) NOT NULL,
	unit VARCHAR(191) NOT NULL,
	PRIMARY KEY(itemId)
);

CREATE TABLE MedicalNeed (
	record INT NOT NULL AUTO_INCREMENT,
	itemId INT UNIQUE,
	CONSTRAINT FK_MEDICALNEED_MEDICALITEM_0 FOREIGN KEY(itemId) REFERENCES MedicalItem(itemId),
	beneficiaryId INT NOT NULL,
	time DATETIME NOT NULL,
	urgency VARCHAR(191) NOT NULL,
	quantity INT NOT NULL,
	PRIMARY KEY('record)
);